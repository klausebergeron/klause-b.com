<h1 align="center" style="font-family: Inconsolata; font-size: 100pt; margin: 5%;">Conveying the Experience of Emotion: Aitmatov and Pirandello’s Use of the Arts</h1>
<h2 align="center" style="font-family: Inconsolata; font-size: 70pt; width: 60%;">3.23.2017</h2>
<p>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;What makes art successful? The world has yet to come to a consensus on what makes a work of art great, be it music, dance, visual, drama, or writing. There is, of course, a canon of great works in all these fields. What I have found to be the mutual element of great artistic pieces is their ability to convey emotion, the universal adhesive of human experience. I have found one of the greatest challenges of writing a narrative to be capturing a true human sentiment in a form which the resonates with the reader. Art is most prominent when emotion is at its peak, either within the artist or by his/her subject. It is thus easy to see how the incorporation of art in a story could help to frame human feeling and interaction in a way that we are familiar with—physically rendered by an artist. The authors of <span style="font-style: italic;">Jamila</span>, and <span style="font-style: italic;">Six Characters in Search of an Author</span>, Chingiz Aitmatov and Luigi Pirandello (respectively) use art to serve this purpose--to link the humanity of the characters together in a way accessible to the characters themselves as well as the reader/audience.  Aitmatov and Pirandello use four distinct forms of the arts to accomplish this. Aitmatov uses song to drive the emotional arc between characters, and visual art to frame the overall sentiment in <span style="font-style: italic;">Jamila</span>. Pirandello, on the other hand, uses authorship to frame the sentiment and dramatic arts to drive the emotional arc in <span style="font-style: italic;">Six Characters in Search of an Author</span>. 
